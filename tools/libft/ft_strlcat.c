#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t n)
{
	size_t	i;
	size_t	j;

	if (!n && !dst)
		return (ft_strlen(src));
	i = ft_strlen(dst);
	j = 0;
	if (n <= i)
		return (ft_strlen(src) + n);
	while (j + i < n - 1 && src[j] != '\0')
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	return (i + ft_strlen(src));
}
