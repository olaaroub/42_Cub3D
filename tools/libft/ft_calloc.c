#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ret;

	if (size != 0 && count > SIZE_MAX / size)
		return (NULL);
	ret = malloc(count * size);
	if (!ret)
		return (NULL);
	ft_bzero(ret, count * size);
	return (ret);
}
